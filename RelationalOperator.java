package operators;

public class RelationalOperator {

	public static void main(String[] args) {
		// (==,<=,>=,!=)
		int a=10;
		int b=20;
		//equality oprt
          System.out.println(a == b);
          
          //non equality oprt
          System.out.println(a != b);
          
          //less than oprt
          System.out.println(a < b);
          
          //gtraeter than oprt
          System.out.println(a > b);
          
          //less than or equal to oprt
          System.out.println(a <= b);
          
          //greater than or equal to oprt  
          System.out.println(a >= b);
      
          
  

	}

}